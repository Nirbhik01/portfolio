// Fetches the GitHub contribution calendar for a user and bakes it into
// src/config/githubActivity.ts, so the site can render a contribution heatmap
// without any runtime external calls (keeping the strict CSP intact).
//
// Run in CI before `npm run build`. Requires a token in GH_TOKEN or
// GITHUB_TOKEN (the Actions-provided token works for reading public data).
// Fails safe: on any error it leaves the existing committed data in place.
import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const login = process.env.GITHUB_ACTIVITY_LOGIN || 'Nirbhik01';
const token = process.env.GH_TOKEN || process.env.GITHUB_TOKEN;
const outFile = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'config', 'githubActivity.ts');

const QUERY = `query($login:String!){
  user(login:$login){
    contributionsCollection{
      contributionCalendar{
        totalContributions
        weeks{ contributionDays{ date contributionCount } }
      }
    }
  }
}`;

async function main() {
  if (!token) {
    console.warn('[activity] No GH_TOKEN/GITHUB_TOKEN set — keeping committed fallback data.');
    return;
  }

  const res = await fetch('https://api.github.com/graphql', {
    method: 'POST',
    headers: {
      Authorization: `bearer ${token}`,
      'Content-Type': 'application/json',
      'User-Agent': `${login}-portfolio`,
    },
    body: JSON.stringify({ query: QUERY, variables: { login } }),
  });

  if (!res.ok) throw new Error(`GitHub API responded ${res.status} ${res.statusText}`);
  const json = await res.json();
  if (json.errors) throw new Error(JSON.stringify(json.errors));

  const calendar = json.data?.user?.contributionsCollection?.contributionCalendar;
  if (!calendar) throw new Error('No contribution calendar in response');

  const weeks = calendar.weeks.map((w) =>
    w.contributionDays.map((d) => ({ date: d.date, count: d.contributionCount })),
  );
  const data = {
    total: calendar.totalContributions,
    weeks,
    fetchedAt: new Date().toISOString(),
    login,
  };

  const contents =
    `// AUTO-GENERATED by scripts/fetch-github-activity.mjs — do not edit by hand.\n` +
    `// This committed copy is the fallback used for local dev/tests; CI overwrites\n` +
    `// it with live contribution data before each production build.\n` +
    `import type { GithubActivity } from './githubActivityTypes';\n\n` +
    `export const githubActivity: GithubActivity = ${JSON.stringify(data, null, 2)};\n`;

  writeFileSync(outFile, contents);
  console.log(`[activity] Wrote ${weeks.length} weeks, ${data.total} contributions for @${login}.`);
}

main().catch((err) => {
  console.warn('[activity] Fetch failed, keeping existing data:', err.message);
  process.exit(0); // never break the build over activity data
});
